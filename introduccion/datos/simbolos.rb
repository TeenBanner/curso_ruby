# simbolos

# un simbolo es una cadena inmutable es decir no se puede modificar en tiempo de ejecucion

# la diferencia entre un simbolo y un string es el performance de la aplicacion ya que a nivel interno el object_id es diferente de string a otro mientras que un sibolo siempre ba a tener el mismo object id

# es recomendable usar simbolos cuando:

# 1- no va a ser nesesario modificar el string
# 2- cuando no se nesecitan los metodos del string
